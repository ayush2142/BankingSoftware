    private void jSquarePyramidVolumeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSquarePyramidVolumeButtonActionPerformed
        if(InputVerification()){
            // The following lines extract the user inputs and saved them to the memory:
            double input_1 = Double.valueOf(jInputField1.getText());// Radius input
            double input_2 = Double.valueOf(jInputField2.getText());// Height input
            // This performs the calculation to find the volume of the sqaure base pyramid:
            double output = power(input_1,2) * (input_2/3); // Volume of sqaure base pyramid is "square_base_area * height/3"
            // This provides the output in the text field:
            jOutputField.setText(String.valueOf(String.format("%.4f", output)));
        }
        else{
            jOutputField.setText("Invalid Input type detected!");
        }
    }//GEN-LAST:event_jSquarePyramidVolumeButtonActionPerformed

