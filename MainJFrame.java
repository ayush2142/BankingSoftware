    private void jLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLoginButtonActionPerformed
        /** 
         * The following is the login button's function, this button checks the credentials of the users
         * and cross-checks the same with a file in the project directory called "Login.txt" for all user scenarios.
         * 
         * It also manipulates the tab for the user based on their role that is selected from the rolebox drop-down menu.
         **/
        
        jStatusMessageLabel.setText(jNameField.getText() + " is logged in.");
        if(user.isUser(jNameField.getText(), jPasswordTextField.getText(),(String)jRoleComboBox.getSelectedItem())){
            String Role = user.RoleVerification(jNameField.getText(),jPasswordTextField.getText(), (String)jRoleComboBox.getSelectedItem());
            
            switch (Role){
                case "Employee": // This the tab manipulaton for Employee, once they have logged in.
                    jMainTabbedPane.addTab("Calculator", jTabInMemory);
                    jMainTabbedPane.addTab("Head Office", jTabInHeadOfficeMemory);
                    break;
                    
                case "Manager": // This the tab manipulaton for Manager, once they have logged in.
                    jMainTabbedPane.addTab("Calculator", jTabInMemory);
                    jMainTabbedPane.addTab("Head Office", jTabInHeadOfficeMemory);
                    jMainTabbedPane.addTab("Person", jTabInPersonMemory);
                    jMainTabbedPane.addTab("Edit Head Office", jTabInEditHeadOfficeMemory);
                    break;
                    
                case "Client": // This the tab manipulaton for Client, once they have logged in.
                    jMainTabbedPane.addTab("Calculator", jTabInMemory);
                    break;
                    
                case "Advisor": // This the tab manipulaton for Advisor, once they have logged in.
                    jMainTabbedPane.addTab("Calculator", jTabInMemory);
                    jMainTabbedPane.addTab("Head Office", jTabInHeadOfficeMemory);
                    jMainTabbedPane.addTab("Person", jTabInPersonMemory);
                    break;
                    
                case "Administrator": // This the tab manipulaton for Administrator, once they have logged in.
                    jMainTabbedPane.addTab("Calculator", jTabInMemory);
                    jMainTabbedPane.addTab("Head Office", jTabInHeadOfficeMemory);
                    jMainTabbedPane.addTab("Person", jTabInPersonMemory);
                    break;
            }
        }
        jMainTabbedPane.setSelectedIndex(0);
    }//GEN-LAST:event_jLoginButtonActionPerformed

